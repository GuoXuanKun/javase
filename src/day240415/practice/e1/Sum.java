package day240415.practice.e1;

public class Sum {

    public int add(int a, int b){
        int result = a + b;
        return result;
    }

    public long add(long a, long b){
        long result = a + b;
        return result;
    }
    public double add(double a, double b){
        double result = a + b;
        return result;
    }

    public float add(float a, float b){
        float result = a + b;
        return result;
    }


}
